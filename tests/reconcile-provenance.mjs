/* ════════════════════════════════════════════════════════════════════════
   tests/reconcile-provenance.mjs — READ-ONLY provenance reconcile REPORTER (SECTION-SCOPED-RUNS, #1)
   ────────────────────────────────────────────────────────────────────────
   The reconcile DANCE (read the settled manifestHash → decide export-inert vs real-output-change →
   hand-edit BUILD-MANIFEST.json + FIXTURE-PROVENANCE.json) is the biggest time/token sink in the
   re-bundle loop. This tool kills the *figuring-out* 90% of that cost WITHOUT touching a byte:

       node tests/reconcile-provenance.mjs               # report all 8 bundles
       node tests/reconcile-provenance.mjs --bundle=oxydex   # scope to one dex (same grammar as the gates)

   For each bundle it recomputes the CURRENT executed-code manifestHash (the same static projection the
   gates use), compares it to BUILD-MANIFEST.json, and — when they differ — inspects that bundle's
   fixtures to classify the change and print the EXACT edit to make. It emits three verdicts:

     • RECONCILED       current manifestHash == committed. Nothing to do. (Per CLAUDE.md: if the ledger
                        already reconciles, do NOT hand-edit it — you'd just race the concurrent writer.)
     • EXPORT-INERT     manifestHash MOVED but every committed fixture OUTPUT still hashes to its recorded
                        outputHash → the code change didn't move any export. Prints the BUILD-MANIFEST
                        manifestHash edit + the fixture manifestHash re-records (outputHash UNCHANGED).
                        Safe to hand-apply.
     • OUTPUT-MOVED     manifestHash moved AND ≥1 fixture output drifted → a real output change. Does NOT
                        print a hand-edit for the output: fixtures MUST be regenerated by RE-RUNNING the
                        app on its committed inputs and RE-EXPORTING (never hand-edited), then re-recorded.
                        Flags exactly which fixtures + reminds you the equiv/golden leg gates this.

   ⚠️ IT NEVER WRITES. By design (CLAUDE.md PROVENANCE-NONDETERMINISM §2/§4): the platform may rebuild a
   bundle and rewrite the ledgers OUT-OF-BAND, so an auto-writer would race that pipeline and could
   corrupt provenance. This tool only tells you the settled truth + the precise edit; a human/agent
   applies it deliberately AFTER the build settles. Re-run it right before you edit — if it now says
   RECONCILED, the other writer already synced; do nothing.

   Zero deps; shares manifest-gate.js with the gates, so its recompute is byte-identical to what
   verify-provenance.html / verify-manifest.mjs check. Exit 0 always (it's a report, not a gate).
   ════════════════════════════════════════════════════════════════════════ */
import { readFileSync, existsSync } from 'node:fs';
import { createRequire } from 'node:module';
import { webcrypto } from 'node:crypto';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

if (!globalThis.crypto) globalThis.crypto = webcrypto;

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const require = createRequire(import.meta.url);
const ManifestGate = require(join(ROOT, 'manifest-gate.js'));

const C = { reset: '\x1b[0m', red: '\x1b[31m', green: '\x1b[32m', dim: '\x1b[2m', bold: '\x1b[1m', yellow: '\x1b[33m', cyan: '\x1b[36m' };
const paint = (s, c) => (process.stdout.isTTY ? c + s + C.reset : s);

// Scope filter (same grammar as the gates): --bundle=/--group=/first non-flag arg / env.
const FILTER = (() => {
  const a = process.argv.slice(2);
  for (let i = 0; i < a.length; i++) {
    const m = a[i].match(/^--?(?:bundle|group|only|b|g)=(.+)$/i);
    if (m) return m[1];
    if (/^--?(?:bundle|group|only|b|g)$/i.test(a[i]) && a[i + 1]) return a[i + 1];
    if (!a[i].startsWith('-')) return a[i];
  }
  return process.env.DEX_BUNDLE || process.env.DEX_GROUP || '';
})();
function matcher(filter) {
  if (!filter) return () => true;
  const tests = String(filter)
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean)
    .map((t) => {
      let rx = null;
      try {
        rx = new RegExp(t, 'i');
      } catch (_) {
        rx = null;
      }
      const lc = t.toLowerCase();
      return (s) => {
        s = String(s == null ? '' : s);
        return rx ? rx.test(s) : s.toLowerCase().indexOf(lc) >= 0;
      };
    });
  return (name) => tests.some((fn) => fn(name));
}

async function main() {
  const _m = matcher(FILTER);
  const BUNDLES = FILTER ? ManifestGate.MANIFEST_BUNDLES.filter(_m) : ManifestGate.MANIFEST_BUNDLES;

  console.log(
    paint('Tepna reconcile-provenance', C.bold) +
      paint('  — READ-ONLY report (never writes)', C.dim) +
      (FILTER ? paint('  · filter "' + FILTER + '" → ' + BUNDLES.length + ' bundle(s)', C.yellow) : '')
  );
  if (FILTER && !BUNDLES.length) {
    console.log(paint('  filter matched ZERO bundles — check the pattern', C.red));
    return;
  }

  // committed ledgers — reassembled from the per-app provenance/ fragments (P3)
  let manifest, fixprov;
  try {
    const _led = require(join(ROOT, 'provenance-ledger.js')).loadNode({ readFileSync }, { join }, ROOT);
    manifest = _led.buildManifest;
    fixprov = _led.fixtureProvenance;
  } catch (e) {
    console.error(paint('✕ provenance/ ledger parse: ' + e.message, C.red));
    return;
  }
  const committed = (manifest && manifest.bundles) || {};
  const fixtures = (fixprov && fixprov.fixtures) || {};

  let anyAction = false;

  for (const bundle of BUNDLES) {
    const p = join(ROOT, bundle);
    const committedMH = committed[bundle] && committed[bundle].manifestHash;
    if (!existsSync(p)) {
      console.log('\n' + paint('● ' + bundle, C.bold) + paint('  bundle file MISSING on disk — cannot recompute', C.red));
      anyAction = true;
      continue;
    }
    const currentMH = await ManifestGate.manifestHashFromText(readFileSync(p, 'utf8'));

    // fixtures produced by THIS bundle (code-gated only; historical snapshots aren't code-reproducible)
    const myFix = Object.keys(fixtures)
      .filter((k) => k[0] !== '_' && fixtures[k] && fixtures[k].bundle === bundle && !fixtures[k].historical)
      .map((k) => ({ name: k, rec: fixtures[k] }));

    if (currentMH === committedMH) {
      console.log('\n' + paint('● ' + bundle, C.bold) + '  ' + paint('✓ RECONCILED', C.green) + paint('  manifestHash ' + currentMH + ' — nothing to do', C.dim));
      continue;
    }

    anyAction = true;
    console.log('\n' + paint('● ' + bundle, C.bold) + '  ' + paint('manifestHash MOVED', C.yellow) + paint('  committed ' + (committedMH || '(none)') + '  →  current ' + currentMH, C.dim));

    // classify: did any committed fixture OUTPUT change? (output-drift => real output change)
    let outMoved = [],
      outSame = [],
      outAbsent = [];
    for (const f of myFix) {
      const outName = f.name; // fixtures keyed by the committed output file name
      const op = join(ROOT, 'uploads', outName);
      if (!existsSync(op)) {
        outAbsent.push(f);
        continue;
      }
      const outNow = await ManifestGate.sha16(new Uint8Array(readFileSync(op)));
      if (f.rec.outputHash && outNow !== f.rec.outputHash) outMoved.push({ ...f, outNow });
      else outSame.push({ ...f, outNow });
    }

    if (outMoved.length) {
      console.log('  ' + paint('⟶ OUTPUT-MOVED', C.red) + '  ' + outMoved.length + ' fixture output(s) drifted — this is a REAL output change.');
      console.log(paint('    DO NOT hand-edit outputs. REGENERATE by re-running the app on its committed inputs and re-exporting,', C.dim));
      console.log(paint('    then re-record { manifestHash, inputHashes, outputHash } for each. The equiv/golden leg gates this.', C.dim));
      for (const f of outMoved) console.log(paint('      • ' + f.name + '  output ' + f.rec.outputHash + ' → ' + f.outNow + '  (regenerate; do not paste this hash)', C.yellow));
      if (outSame.length) console.log(paint('    (also re-record manifestHash on the same-output siblings below — same code moved them: ' + outSame.map((f) => f.name).join(', ') + ')', C.dim));
    } else if (outAbsent.length === myFix.length && myFix.length) {
      console.log(
        '  ' +
          paint('⟶ CANNOT CLASSIFY', C.yellow) +
          '  all ' +
          myFix.length +
          ' fixture output(s) absent (uploads/ gitignored?). Re-run where uploads/ is served (the browser page) to classify inert vs moved.'
      );
    } else {
      console.log('  ' + paint('⟶ EXPORT-INERT', C.green) + '  every committed fixture output is unchanged — the code change did not move any export.');
    }

    // The exact BUILD-MANIFEST edit (always needed once the hash moved and it's the settled value)
    console.log(paint('    EDIT BUILD-MANIFEST.json → bundles["' + bundle + '"].manifestHash:', C.cyan));
    console.log(paint('      "' + (committedMH || '') + '"  →  "' + currentMH + '"', C.bold));

    // Fixture manifestHash re-records (both inert siblings and moved ones carry the new code identity)
    const reRecord = outMoved.length ? myFix : outSame; // when output moved, ALL of this bundle's fixtures re-record
    if (reRecord.length) {
      console.log(
        paint(
          '    RE-RECORD FIXTURE-PROVENANCE.json fixtures[<name>].manifestHash → "' +
            currentMH +
            '"' +
            (outMoved.length ? '  (AND regenerate outputs first — see above)' : '  (outputHash UNCHANGED — inert):'),
          C.cyan
        )
      );
      for (const f of reRecord) console.log(paint('      • ' + f.name + '   ' + (f.rec.manifestHash || '(none)') + ' → ' + currentMH, C.dim));
    }
  }

  console.log('\n' + paint('══════════════════════════════════════════', C.dim));
  if (!anyAction) console.log(paint('✓ ALL RECONCILED — no ledger edit needed (do NOT hand-edit a synced ledger).', C.green));
  else {
    console.log(paint('▸ Report only — NOTHING was written.', C.yellow) + paint('  Apply the edits above AFTER the build settles, then re-run this', C.dim));
    console.log(paint('  to confirm it now says RECONCILED. If it already flipped to RECONCILED, the concurrent writer synced it — do nothing.', C.dim));
    console.log(paint('  Verify with: node tests/verify-manifest.mjs' + (FILTER ? ' --bundle=' + FILTER : '') + '   (+ the equiv/golden leg in Dex-Test-Suite.html for OUTPUT-MOVED).', C.dim));
  }
}

main();
